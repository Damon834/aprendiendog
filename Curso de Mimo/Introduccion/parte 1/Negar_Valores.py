# El codigo not esta delante de True hace que la expresion resulte en False. Si algo no es ciero, tiene que ser False
# not es el operador de negacion. Convierte los valores en su opuesto 

# Cuando cambiamos un valor a su operador con not, lo negamos

print ( not True) 

# El operador not antes de False cambia su valor. Si un valor no es False, tiene que ser True 

print ( not False)

# Podemos usar el operador not con variables para negar sus valores. Al mostar not availabre aqui, veremos su valor negado 

availabre = True
print ( not availabre)

# Tambien podemos guardar una negacion completa en otra variabla 

morning = True
is_evenig = not morning
print ( is_evenig)

