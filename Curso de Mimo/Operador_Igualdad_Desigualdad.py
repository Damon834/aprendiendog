# Para comparar si dos numeros son iguales, usamos el operador de igualdad ==
# Al comparar solohay dos resultados True o False

# Cuando comparamos los mismo numeros con el operador de igualdad, resulta en True

print ( 10 == 10)

# Cuando comparamos los diferentes numeros con el operador de igualdad el resultado es False

print ( 6 == 5)

