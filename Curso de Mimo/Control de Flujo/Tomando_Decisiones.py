# Usamos if para escribir codigo que responda a diferentes situaciones. Lo reconocemos por la palabra clave if 

if True:
    print("Hello")

# En lugar de usar el valor booleano True, podemos guardalo en una variable como greet y usarlo como condicion 

greet = True
if greet:
    print("Hello Mundo")

# el uso de una variable como gret establecida en False nos permite omitir codigo como la declaracion de visualizacion

gret = False
if gret:
    print ("hola mundo")