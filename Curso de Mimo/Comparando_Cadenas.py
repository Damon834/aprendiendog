# Podemos usar comparaciones para comparar si un string es igual o no a otro string

print ("online" == "online")
print ("inline" != "offline")

# Para comparar si un string es igual a otro string, tambiel utilizamos el operador de igual ==

print ("apple" == "apple")

# Si el string de la izquierda no es igual al string de la derecha, el resultado es falso

print ("fresa" == "naranja")

# Tambien podemos comparar variable que almacena cadenas entre si 

fruta1 = " patilla "
fruta2 = " mango "
print ( fruta1 == fruta2)

# Para comparar si un sting no es igual a otro string, se utiliza el operador de desigualdad !=

print ("subscribed" != "rejected")

# Si el string de la izquierda es igual al string de la dercha, el resultado es False

print ("suscribete" != "suscribete")

# Para almacenar el resultado de una comparacion de una variable, usamos el signo 

same = "youtube" != "youtube"
print (same)
